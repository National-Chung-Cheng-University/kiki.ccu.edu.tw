function SelectAction(Option){
   if(Option==0){
        document.MainMenuForm.action="Main.cgi";
        document.MainMenuForm.submit();
   }
   if(Option==1){
        document.MainMenuForm.action="Add_Course00.cgi";
        document.MainMenuForm.submit();
   }
   if(Option==2){
        document.MainMenuForm.action="Del_Course00.cgi";
        document.MainMenuForm.submit();
   }
   if(Option==3){
        document.MainMenuForm.action="Change_Password00.cgi";
        document.MainMenuForm.submit();
   }
   if(Option==4){
        document.MainMenuForm.action="Selected_View00.cgi";
        document.MainMenuForm.submit();
   }
   if(Option==5){        
        document.MainMenuForm.action="../../Update_Course.html";
        document.MainMenuForm.method="GET";
        document.MainMenuForm.submit();
   }
   if(Option==6){
   	document.MainMenuForm.action="Print_Course.cgi";
	document.MainMenuForm.submit();
   }
   if(Option==7){
        document.MainMenuForm.action="View_Warning.cgi";
        document.MainMenuForm.submit();
   }
}

